			on e.id = r.pyEmployeeid
			inner join PayrollRun pr on pr.id = e.payrollRunid
			where pr.DateTobePrinted between @StartDate and @EndDate
			and r.pyResultTypeid in (56)
			group by e.ReferenceKey
